'''
基础需求：
让用户输入用户名密码
认证成功后显示欢迎信息
输错三次后退出程序

升级需求：
可以支持多个用户登录 (提示，通过列表存多个账户信息)
用户3次认证失败后，退出程序，再次启动程序尝试登录时，还是锁定状态（提示:需把用户锁定的状态存到文件里）

基础需求伪代码：
定义 User 类:
    属性:
        用户名
        密码

函数 登录():
    尝试次数 = 0
    最大尝试次数 = 3
    加载用户数据()

    当 尝试次数 < 最大尝试次数:
        输入 用户名
        输入 密码

        如果 用户名和密码匹配:
            显示 欢迎信息
            结束 登录 函数
        否则:
            尝试次数 += 1
            显示 错误信息

    如果 尝试次数 >= 最大尝试次数:
        显示 锁定信息
        结束 程序

主程序:
    调用 登录() 函数


升级需求伪代码：
定义 User 类:
    属性:
        用户名
        密码
        是否锁定 (默认为 False)

函数 加载用户数据():
    尝试 从文件读取用户列表
        如果 文件存在:
            解析 文件内容 到 用户列表
        否则:
            初始化 用户列表 为空

函数 保存用户数据(用户列表):
    将 用户列表 序列化 并 写入文件

函数 登录():
    尝试次数 = 0
    最大尝试次数 = 3
    加载用户数据()

    当 尝试次数 < 最大尝试次数:
        输入 用户名
        输入 密码

        在 用户列表 中 查找 用户名 对应的 用户对象
        如果 找到用户对象 并且 用户未被锁定 并且 用户名和密码匹配:
            显示 欢迎信息
            结束 登录 函数
        否则:
            如果 用户被锁定:
                显示 账户已锁定的信息
                结束 登录 函数
            尝试次数 += 1
            显示 错误信息

    如果 尝试次数 >= 最大尝试次数:
        在 用户列表 中 查找 用户名 对应的 用户对象
        如果 找到用户对象 并且 用户未被锁定:
            设置 用户对象 的 是否锁定 属性 为 True
            保存用户数据(用户列表)
        显示 锁定信息
        结束 程序

主程序:
    调用 登录() 函数
'''